public class Num6 {
    public static void main(String[] args) {
        int [] arr = new int [] {};
        int sum = 0;
        for (int i = 0; i < arr.length; i++) { //for loop to calculate the sum
            sum = sum + arr[i];
        }
        System.out.println("Sum of all the elements in arr is: " + sum);
    }
}
